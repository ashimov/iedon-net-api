exports.nullOrEmpty = obj => obj === undefined || obj === null || obj === '';

const jwt = require('jsonwebtoken');
exports.signAsync = (obj, secret, options) => new Promise((resolve, reject) => 
    jwt.sign(obj, secret, options, (error, token) => {
        if (error) {
            reject(error);
            return;
        }
        resolve(token);
    })
);

exports.verifyAsync = (token, secret, options) => new Promise((resolve, reject) =>
    jwt.verify(token, secret, options, (error, decoded) => {
        if (error) {
            reject(error);
            return;
        }
        resolve(decoded);
    })
);

exports.getRandomCode = () => `DN42_VERIFICATION_${(Math.random() + 1).toString(36).substring(2)}${(Math.random() + 1).toString(36).substring(2)}`;

const bcrypt = require('bcrypt');
exports.bcryptGenSalt = () => new Promise((resolve, reject) =>
    bcrypt.genSalt(10, function(error, salt) {
        if (error) reject(error);
        resolve(salt);
    })
);

exports.bcryptGenHash = (text, salt) => new Promise((resolve, reject) =>
    bcrypt.hash(text, salt, function(error, hash) {
        if (error) reject(error);
        resolve(hash);
    })
);

exports.bcryptCompare = (text, hash) => new Promise((resolve, reject) =>
    bcrypt.compare(text, hash, function(error, result) {
        if (error) reject(error);
        resolve(result);
    })
);

